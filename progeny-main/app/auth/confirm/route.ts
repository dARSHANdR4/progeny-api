import { type EmailOtpType } from '@supabase/supabase-js'
import { type NextRequest, NextResponse } from 'next/server'
import { createClient } from '@/lib/supabase/server'

export async function GET(request: NextRequest) {
    const { searchParams } = new URL(request.url)
    const code = searchParams.get('code')
    const token_hash = searchParams.get('token_hash')
    const type = searchParams.get('type') as EmailOtpType | null
    const next = searchParams.get('next') ?? '/auth/confirmed'

    const redirectTo = request.nextUrl.clone()
    redirectTo.pathname = next
    redirectTo.searchParams.delete('code')
    redirectTo.searchParams.delete('token_hash')
    redirectTo.searchParams.delete('type')

    if (code) {
        const supabase = await createClient()
        const { error } = await supabase.auth.exchangeCodeForSession(code)
        if (!error) {
            return NextResponse.redirect(redirectTo)
        }
        console.error('Auth error exchanging code:', error)
    }

    if (token_hash && type) {
        const supabase = await createClient()
        const { error } = await supabase.auth.verifyOtp({
            type,
            token_hash,
        })
        if (!error) {
            return NextResponse.redirect(redirectTo)
        }
        console.error('Auth error verifying OTP:', error)
    }

    // Return the user to an error page if verification fails
    // But for better UX, if it's already verified, Supabase might error, 
    // so we still redirect to success if we're not sure, or a generic error page.
    // Let's redirect to a simple error page that we'll create or just back to login.
    redirectTo.pathname = '/auth/login'
    redirectTo.searchParams.set('error', 'Verification link invalid or expired')
    return NextResponse.redirect(redirectTo)
}
